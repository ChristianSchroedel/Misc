    {
        String path = "/assets/";
        
        images = new ArrayList<ImageIcon>();
        images.add(new ImageIcon(this.getClass().getResource(path + "BeanAdd24.gif")));
        images.add(new ImageIcon(this.getClass().getResource(path + "Cut24.gif")));
        images.add(new ImageIcon(this.getClass().getResource(path + "Delete24.gif")));
        images.add(new ImageIcon(this.getClass().getResource(path + "Find24.gif")));
        images.add(new ImageIcon(this.getClass().getResource(path + "Help24.gif")));
        images.add(new ImageIcon(this.getClass().getResource(path + "Home24.gif")));
        images.add(new ImageIcon(this.getClass().getResource(path + "Host24.gif")));
        images.add(new ImageIcon(this.getClass().getResource(path + "Volume24.gif")));
        images.add(new ImageIcon(this.getClass().getResource(path + "WebComponent24.gif")));
    }
    
    /**
     * Select icon
     * @param number Requested item
     * @return Selected icon
     */
    public ImageIcon selectIcon(int number)
    {
        return images.get(number-1);
    }

